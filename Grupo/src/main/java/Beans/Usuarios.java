package Beans;

public class Usuarios {

    private int codigopucp;;
    private String nombre;
    private String apellido;
    private int edad;
    private String correopucp;
    private String especialidad;
    private String contrasenia;
    private String status;

    public int getCodigopucp() {
        return codigopucp;
    }

    public void setCodigopucp(int codigopucp) {
        this.codigopucp = codigopucp;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getCorreopucp() {
        return correopucp;
    }

    public void setCorreopucp(String correopucp) {
        this.correopucp = correopucp;
    }

    public String getEspecialidad() {
        return especialidad;
    }

    public void setEspecialidad(String especialidad) {
        this.especialidad = especialidad;
    }

    public String getContrasenia() {
        return contrasenia;
    }

    public void setContrasenia(String contrasenia) {
        this.contrasenia = contrasenia;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
