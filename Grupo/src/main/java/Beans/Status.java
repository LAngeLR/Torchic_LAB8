package Beans;

public class Status {
    private int idstatus;
    private String status;
    private int gasto_asociado_min;

    public int getIdstatus() {
        return idstatus;
    }

    public void setIdstatus(int idstatus) {
        this.idstatus = idstatus;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getGasto_asociado_min() {
        return gasto_asociado_min;
    }

    public void setGasto_asociado_min(int gasto_asociado_min) {
        this.gasto_asociado_min = gasto_asociado_min;
    }
}
